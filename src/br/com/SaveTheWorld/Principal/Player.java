package br.com.SaveTheWorld.Principal;

public class Player {

	private char nome;
	private float gold;
	private int Lvl;
	
//MÉTODOS GETTERS E SETTERS
	
	public char getNome() {
		return nome;
	}
	public void setNome(char nome) {
		this.nome = nome;
	}
	public float getGold() {
		return gold;
	}
	public void setGold(float gold) {
		this.gold = gold;
	}
	public int getLvl() {
		return Lvl;
	}
	public void setLvl(int lvl) {
		Lvl = lvl;
	}
	
	
	
}
